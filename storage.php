				<h2>Vehicle Storage Suggestions</h2>
				<p class="well">A garage provides protection from the elements and keeps the vehicle at a relatively stable temperature making it the ideal place for long term storage.</p>
				<h3>Protecting your vehicle during long-term storage (More than 30 days)</h3>
				<ul>
				<li>Protect the paint of the vehicle from water stains and bird droppings by giving it a thorough wash prior to storing.  A coat of wax can provide extra protection.</li>
				<li>Clean wheels and undercarriage to remove mud, grease, and tar.  Road salt, used to melt snow and ice during inclement weather, can cause rust and corrosion of the frame, exhaust and suspension components while the car, or truck, is being stored.</li>
				<li>Some car manufacturers recommend changing the oil to prevent the contaminants found in used oil from damaging the engine.</li>
				<li>Prevent moisture from accumulating inside the fuel tank, and drying out the seals, by filling the vehicle with gas.</li>
				<li>Protect the engine from gum, varnish, rust and ethanol buildup by treating the gas with a fuel stabilizer each year the vehicle is in storage.</li>
				<li>Disconnect the negative battery cable to prevent the battery from losing its charge due to lack of use. Keep in mind that stereo presets, clocks, and other settings will need to be reset after storing the vehicle.</li>
				<li>Disengage the parking brake when the car is in storage. A brake pad in contact with a rotor may fuse when a vehicle sits unused in storage for a lengthy period of time. Consider chocking the tires, using appropriate methods, to prevent the car from moving.</li>
				<li>Prevent flat spots on the tires The weight of the vehicle pressing down on the small footprints of the tire in contact with the garage floor may cause flat spots. In severe cases, a flat spot can cause permanent damage to the tire requiring it to be replaced.<ul>
				<li>Place the car on jack stands at all four corners to prevent flat spots</li>
				<li>Inflate the tires to the recommended pressure when in storage may help prevent flat spots.  Vehicles stored in colder temperatures or equipped with performance tires are most vulnerable.</li></ul></li>
				<li>A dry and relatively warm garage can be attractive to rodents. Close all doors and windows to prevent mice or rats from damaging your vehicle while in storage. Consult your local hardware store for preventive measures you can take against rodents.</li>
				<li>Contact your insurance company to determine if maintaining your coverage is the best option or not.  Some providers may raise rates due to the gap in your coverage. </li></ul>
				<h3>Bringing your vehicle out of long-term storage</h3>
				<p class="well">What to do after returning a stored vehicle to active use.</p>
				<ul>
				<li>Look for any evidence of rodents such as chewed upholstery, carpet, hoses, belts, or wires</li>
				<li>Inspect the windshield wipers for cracked or brittle rubber</li>
				<li>Remove jack stands if used, otherwise inspect the tires for flat spots</li> 
				<li>Check tires for the recommended pressure</li>
				<li>Check the brakes</li>
				<li>Check that all fluids are at the recommended levels</li>
				<li>Reconnect the battery if disconnected, making sure to clean each terminal</li>
				</ul>
